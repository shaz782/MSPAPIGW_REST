package com.xius.rmsp.messages;

public class MSISDNInfo {

	private String msisdnNo;
	private String simNo;
	private String status;
	/**
	 * @return the msisdnNo
	 */
	public String getMsisdnNo() {
		return msisdnNo;
	}
	/**
	 * @param msisdnNo the msisdnNo to set
	 */
	public void setMsisdnNo(String msisdnNo) {
		this.msisdnNo = msisdnNo;
	}
	/**
	 * @return the simNo
	 */
	public String getSimNo() {
		return simNo;
	}
	/**
	 * @param simNo the simNo to set
	 */
	public void setSimNo(String simNo) {
		this.simNo = simNo;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
